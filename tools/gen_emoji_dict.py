def generate_emoji_dict():
    output_dir = "dicts/emoji"
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)
    
    final_dict = {}

    for emoji, keywords in EXACT_MAP.items():
        kw_list = keywords.split(',')
        for kw in kw_list:
            # 转换为全拼
            pys = lazy_pinyin(kw)
            if pys:
                py = "".join(pys)
                if py not in final_dict:
                    final_dict[py] = []
                
                # 检查是否已存在
                if not any(e["char"] == emoji for e in final_dict[py]):
                    final_dict[py].append({
                        "char": emoji,
                        "trad": emoji,
                        "en": kw,
                        "category": "emoji",
                        "weight": 1000
                    })

    output_file = os.path.join(output_dir, "emoji.json")
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(final_dict, f, ensure_ascii=False, indent=2)
    
    print(f"Precise Emoji dictionary generated at {output_file} with {len(final_dict)} Pinyin keys.")

if __name__ == "__main__":
    generate_emoji_dict()
